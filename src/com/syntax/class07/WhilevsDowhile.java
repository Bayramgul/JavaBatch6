package com.syntax.class07;

public class WhilevsDowhile {

	public static void main(String[] args) {
		int num=1;
		while (num<=5) {
			System.out.println("Hello");
			num++;
		}
		System.out.println("-------Using Do while Loop");
		int num1=1;
		do {
			System.out.println("Hello");
			num1++;
		}while(num1<=5);
		
	}
	

}

package com.syntax.class07;

public class ForLoops {

	public static void main(String[] args) {
		for(int i=1; i<=5;i++) {
			System.out.println("Good Morning");
		}
		//what will be the output
		for(int i=0; i<=50;i+=5) {
			System.out.println(i);
		}
		int sum=0;
		for (int i=1;i<5;i++) {
			sum=sum+i;
			
		}System.out.println(sum);
	}

}
